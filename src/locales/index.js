export const defaultLocale = 'en-US'
export const fallbackLocale = 'en-US'
export const localeStorageKey = 'biotrack.locale'

export function normalizeLocale(locale) {
  const normalized = String(locale ?? '').replace('_', '-')
  if (normalized.toLowerCase().startsWith('es')) return 'es-419'
  return 'en-US'
}

export function resolveInitialLocale() {
  const storedLocale = localStorage.getItem(localeStorageKey)
  if (storedLocale) return normalizeLocale(storedLocale)
  return defaultLocale
}

